    if (T != T_save)
    {
        T_save = T;
        comp_k_f(tc,invT,k_f_save);
        comp_Kc(tc,invT,Kc_save);
    }

    double q_f[0], q_r[0];
    comp_qfqr(q_f, q_r, sc, tc, invT);

